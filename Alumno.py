class Alumno:

    def __init__(self):
        self.cuenta = ""
        self.nombre = ""
    
    def __init__(cuenta, nombre):
        self.cuenta = cuenta
        self.nombre = nombre
    
    def setNombre(nombre):
        self.nombre = nombre
    def getNombre():
        return nombre

    def setCuenta(cuenta):
        self.cuenta = cuenta
    def getCuenta():
        return self.cuenta

    def __str__():
        return self.cuenta + " " + self.nombre 